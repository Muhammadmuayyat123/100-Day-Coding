void main() {
  int mahasiswa = 250;
  int dosen = 200;

  print("$mahasiswa > $dosen: ${mahasiswa > dosen}");
  print("$mahasiswa < $dosen: ${mahasiswa < dosen}");
  print("$mahasiswa >= $dosen: ${mahasiswa >= dosen}");
  print("$mahasiswa <= $dosen: ${mahasiswa <= dosen}");
  print("$mahasiswa == $dosen: ${mahasiswa == dosen}");
  print("$mahasiswa != $dosen: ${mahasiswa != dosen}");
}
